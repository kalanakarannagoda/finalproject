     public void load_employee(){
          try {
             ResultSet rs = DBMS.search("SELECT * FROM employee");
              TableModel tm = jTable3.getModel();
              DefaultTableModel dtm = (DefaultTableModel) tm;
              dtm.setRowCount(0);
              while(rs.next()){
                  Vector<String> v = new Vector<>();
                  v.add(rs.getString("mobile"));
                  v.add(rs.getString("nic"));
                  v.add(rs.getString("name"));
                 ResultSet rs2 = DBMS.search("SELECT `city` FROM city WHERE id = '"+rs.getString("city_id")+"'");
                 if(rs2.next()){
                     v.add(rs2.getString("city"));
                 }
                 v.add(rs.getString("date_registered"));
                 dtm.addRow(v);
              }
          } catch (Exception e) {
              e.printStackTrace();
          }
      }
