    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        String mobile = jTextField1.getText();
        String name =jLabel5.getText();
        String nic = jLabel6.getText();
        String workdays= jTextField2.getText();
        String onedaysalary= jTextField3.getText();
        String total = jLabel10.getText();
        if(!mobile.isEmpty() && name!="None" && nic!="None" && !workdays.isEmpty() && !onedaysalary.isEmpty() && total!="00.00"){
        try {
            String path ="src/report/pay.jasper";
            JREmptyDataSource ds = new JREmptyDataSource();
            HashMap<String,Object> map = new HashMap<>();
            map.put("Parameter1",name);
            map.put("Parameter2",nic);
            map.put("Parameter3",mobile);
            map.put("Parameter4",workdays);
            map.put("Parameter5",onedaysalary);
            map.put("Parameter6",total);
            JasperPrint print = JasperFillManager.fillReport(path, map, ds);
            JasperViewer.viewReport(print,false);
        } catch (Exception e) {
        e.printStackTrace();
        }
        }else{
            JOptionPane.showMessageDialog(this,"Please Enter Employee Data","Empty Data",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton9ActionPerformed

